package operators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println(12+13); // 25
        System.out.println(25-13); // 12
        System.out.println(12*13); // 156
        System.out.println(13/12); // 1 --> Divided in one times.
        System.out.println(25%13); // 12 --> Remainder
    }
}
