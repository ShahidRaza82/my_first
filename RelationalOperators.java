package operators;

public class RelationalOperators {
    public static void main(String[] args){
        // >, <, ==, >=, <=, !=
        System.out.println(45>25); // true
        System.out.println(45<25); // false
        System.out.println(67==67); // true
        System.out.println(45>=35); // true
        System.out.println(45<=35); // false
        System.out.println(34<=67); // true
        System.out.println(34>=67); // false
        System.out.println(45!=34); // true
        System.out.println(54!=54); // false
    }
}
